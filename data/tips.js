/**
 * 根据阶段随机获取一条提示
 * @param {string} phase - 经期阶段：menstrual/follicular/ovulation/luteal/general
 * @returns {object} 提示对象 { id, text, icon }
 */
function getRandomTip(phase) {
  const pool = tips[phase] || tips.general
  return pool[Math.floor(Math.random() * pool.length)]
}

/**
 * 获取每日提示（基于日期种子，同一天返回同一条）
 * @param {string} phase - 经期阶段
 * @returns {object} 提示对象 { id, text, icon }
 */
function getDailyTip(phase) {
  const pool = tips[phase] || tips.general
  const dateStr = new Date().toISOString().slice(0, 10)
  let seed = 0
  for (let i = 0; i < dateStr.length; i++) {
    seed += dateStr.charCodeAt(i)
  }
  return pool[seed % pool.length]
}

module.exports = { tips, getRandomTip, getDailyTip }
