/**
 * 获取每日推荐文章（3篇，基于日期种子，同一天返回相同的3篇）
 * @returns {Array} 3篇推荐文章
 */
function getDailyArticles() {
  const dateStr = new Date().toISOString().slice(0, 10)
  let seed = 0
  for (let i = 0; i < dateStr.length; i++) {
    seed += dateStr.charCodeAt(i)
  }
  const result = []
  const used = new Set()
  for (let i = 0; i < 3; i++) {
    let index = (seed + i * 7) % articles.length
    while (used.has(index)) {
      index = (index + 1) % articles.length
    }
    used.add(index)
    result.push(articles[index])
  }
  return result
}

/**
 * 按分类获取文章
 * @param {string} categoryId - 分类ID
 * @returns {Array} 该分类下的所有文章
 */
function getArticlesByCategory(categoryId) {
  return articles.filter((article) => article.categoryId === categoryId)
}

/**
 * 获取文章详情
 * @param {number} id - 文章ID
 * @returns {object|undefined} 文章对象
 */
function getArticleById(id) {
  return articles.find((article) => article.id === id)
}

/**
 * 搜索文章（按标题、摘要、标签匹配）
 * @param {string} keyword - 搜索关键词
 * @returns {Array} 匹配的文章列表
 */
function searchArticles(keyword) {
  if (!keyword || !keyword.trim()) return []
  const kw = keyword.trim().toLowerCase()
  return articles.filter((article) => {
    return (
      article.title.toLowerCase().includes(kw) ||
      article.summary.toLowerCase().includes(kw) ||
      article.tags.some((tag) => tag.toLowerCase().includes(kw))
    )
  })
}

